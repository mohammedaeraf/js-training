let signal = "red"; // Change to "green", "yellow", or any other value

if (signal === "red") {
  console.log("Stop! 🚫");
} else if (signal === "yellow") {
  console.log("Get Ready! ⚠️");
} else if (signal === "green") {
  console.log("Go! ✅");
} else {
  console.log("Invalid signal color. Please check the traffic light.");
}
