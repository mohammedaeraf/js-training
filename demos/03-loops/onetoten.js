// Initialize an empty string to store the numbers
let str = "";

// Loop from 1 to 10, incrementing by 1 each time
for (let i = 1; i <= 10; i++) {
  // Append the current number and a space to the string
  str = str + i + " "; // "1 2 3 "
}
// Print the resulting string of numbers to the console
console.log(str);